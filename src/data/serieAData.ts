export const generateCalendar = (teamName: string = TEAM_NAME, seasonIndex = 1): Match[] => {
  const opponents = TEAMS.filter(team => team !== teamName);

  return Array.from({ length: 38 }, (_, index) => {
    const round = index + 1;
    const opponent = opponents[(round - 1) % opponents.length];
    const firstLegHome = round % 2 === 1;
    const isHome = round <= 19 ? firstLegHome : !firstLegHome;

    return {
      id: `match_${round}`,
      opponent,
      opponentInitials: opponent.split(' ').map(word => word[0]).join('').slice(0, 3).toUpperCase(),
      stadium: isHome ? (STADIUMS[teamName] ?? DEFAULT_CLUB_PROFILE.stadium) : (STADIUMS[opponent] ?? 'Stadio Comunale'),
      isHome,
      date: getDateForRound(round, seasonIndex),
      status: round === 1 ? 'next' : 'future',
      playedIndex: round
    };
  });
};

const getDateForRound = (round: number, seasonIndex = 1): string => {
  const baseDate = new Date(2026 + Math.max(0, seasonIndex - 1), 7, 23);
  baseDate.setDate(baseDate.getDate() + (round - 1) * 7);
  return baseDate.toLocaleDateString('it-IT', { day: '2-digit', month: 'short', year: 'numeric' });
};

