const estimateValue = (overall: number, potential: number, age: number, strength: number) => {
  const ratingBase = Math.max(1, overall - 63);
  const potentialBonus = Math.max(0, potential - overall) * 850000;
  const ageFactor = age <= 23 ? 1.25 : age <= 27 ? 1.1 : age >= 33 ? 0.62 : 0.9;
  const strengthFactor = 0.78 + strength / 250;
  return Math.round(((ratingBase * ratingBase * 110000) + potentialBonus) * ageFactor * strengthFactor / 100000) * 100000;
};

const estimateWage = (overall: number, strength: number) => {
  const base = Math.max(6000, (overall - 62) * 4200);
  return Math.round(base * (0.75 + strength / 170) / 1000) * 1000;
};

export const createPlayersForClub = (club: ClubProfile): Player[] => {
  const meta = getClubMeta(club);
  const seeds = CLUB_ROSTERS[meta.id] ?? CLUB_ROSTERS[DEFAULT_CLUB_PROFILE.id];

  const players = seeds.map((seed, index) => {
    const id = `${meta.id}_p${index + 1}`;
    const personality = buildPlayerPersonality({ id, ...seed }, meta.name, index);
    const relationships = buildPlayerRelationships({ id, ...seed }, personality);

    return {
      id,
      name: seed.name,
      role: seed.role,
      secondaryRoles: seed.secondaryRoles,
      age: seed.age,
      nationality: seed.nationality,
      overall: seed.overall,
      potential: seed.potential,
      form: Number((6.2 + (seed.overall - 70) / 12 + (index % 4) * 0.08).toFixed(1)),
      morale: Math.min(96, 68 + Math.round((seed.overall - 68) * 1.15) + (index % 5)),
      condition: Math.max(88, 100 - (index % 4) * 3),
      stamina: buildPlayerStamina({ id, ...seed }, meta.name, index),
      value: seed.value ?? estimateValue(seed.overall, seed.potential, seed.age, meta.strength),
      wage: estimateWage(seed.overall, meta.strength),
      contractYears: index % 5 === 0 ? 2 : index % 4 === 0 ? 4 : 3,
      status: seed.overall >= 84 ? 'In Forma' as const : seed.age >= 34 ? 'Stanco' as const : 'Disponibile' as const,
      height: seed.height,
      preferredFoot: seed.preferredFoot,
      sourceRole: seed.sourceRole,
      valueLabel: seed.valueLabel,
      attributes: seed.attributes,
      externalProfile: seed.externalProfile,
      personality,
      relationships,
      careerMemory: buildCareerMemory()
    };
  });

  return assignSquadRelationships(players);
};

